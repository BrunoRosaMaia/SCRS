    private void btnEditarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarRegistroActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione um registro para editar.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        List<RegistroVenda> registros = registroDAO.listar();
        RegistroVenda registroSelecionado = registros.get(linhaSelecionada);
        
        TelaRegistro telaRegistro = new TelaRegistro(usuarioAutenticado, registroSelecionado);
        telaRegistro.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnEditarRegistroActionPerformed

    private void btnRemoverRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverRegistroActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um registro para remover.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja remover este registro?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if(confirmacao == JOptionPane.YES_OPTION){
           List<RegistroVenda> registros = registroDAO.listar();
           RegistroVenda registroSelecionado = registros.get(linhaSelecionada);
           registroDAO.excluir(registroSelecionado.getId());
           
           carregarDados();
           JOptionPane.showMessageDialog(this, "Registro removido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverRegistroActionPerformed

