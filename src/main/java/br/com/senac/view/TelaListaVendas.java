    private void permissoes(Usuario usuarioAutenticado) {
        switch (usuarioAutenticado.getTipoUsuario()) {
            case "Securitário":
                btnEditarRegistro.setEnabled(false);
                btnRemoverRegistro.setEnabled(false);
                break;
            case "Gerente":
                btnEditarRegistro.setEnabled(true);
                btnRemoverRegistro.setEnabled(true);
                break;
        }
    }
    
    private void configurarTabela() {
        tableModel = new DefaultTableModel(
            new String[]{"Cliente", "CPF do Cliente", "Data da Venda", "Tipo de Seguro", "Valor da Apólice", "Fiel?"}, 0);
        jTable1.setModel(tableModel);
    }
    
    private void carregarDados() {
        List<RegistroVenda> registros = registroDAO.listar();
        tableModel.setRowCount(0);

        for (RegistroVenda registro : registros) {
            tableModel.addRow(new Object[]{
                registro.getNomeCliente(),
                registro.getCpfCliente(),
                registro.getDataVenda(),
                registro.getTipoSeguro(),
                registro.getValorApolice(),
                registro.isFiel() ? "Sim" : "Não"
            });
        }
    }

