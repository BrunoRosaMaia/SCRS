    private void preencherCampos(){
        if (registroEdicao != null) {
            txtNomeCliente.setText(registroEdicao.getNomeCliente());
            txtCPF.setText(registroEdicao.getCpfCliente());
            txtDataVenda.setText(new SimpleDateFormat("dd/MM/yyyy").format(registroEdicao.getDataVenda()));
            cbxTipoSeguro.setSelectedItem(registroEdicao.getTipoSeguro());
            txtValorApolice.setText(String.valueOf(registroEdicao.getValorApolice()));
            if (registroEdicao.isFiel()) {
                rbtnSim.setSelected(true);
            } 
            else {
                rbtnNao.setSelected(true);
            }
        }
    }

