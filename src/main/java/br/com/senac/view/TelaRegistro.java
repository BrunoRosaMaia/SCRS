    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nomeCliente = txtNomeCliente.getText().trim();
        String cpfCliente = txtCPF.getText().trim();
        String tipoSeguro = (String) cbxTipoSeguro.getSelectedItem();
        String dataVendaStr = txtDataVenda.getText().trim();
        String valorApoliceStr = txtValorApolice.getText().trim();
        boolean fiel = rbtnSim.isSelected();
        
        StringBuilder erros = new StringBuilder();
        if (nomeCliente.isEmpty()) erros.append("- Nome do cliente\n");
        if (cpfCliente.isEmpty()) erros.append("- CPF do cliente\n");
        if (dataVendaStr.isEmpty()) erros.append("- Data da venda\n");
        if (valorApoliceStr.isEmpty()) erros.append("- Valor da apólice\n");
        if (!rbtnSim.isSelected() && !rbtnNao.isSelected()) erros.append("- Cliente é fiel?");
        
        if (erros.length() > 0){
            JOptionPane.showMessageDialog(this, "Os seguintes campos não podem estar vazios:\n" + erros.toString(), "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        double valorApolice;
        try {
            valorApolice = Double.parseDouble(valorApoliceStr);
            if (valorApolice <= 0){
                JOptionPane.showMessageDialog(this, "O valor da apólice deve ser maior que zero.", "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Valor da apólice inválido. Insira um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        valorApolice = PlanoFidelidade.calcularValorComDesconto(tipoSeguro, valorApolice, fiel);
        
        java.util.Date dataVendaUtil = null;
        try {
            SimpleDateFormat formatoEntrada = new SimpleDateFormat("dd/MM/yyyy");
            dataVendaUtil = formatoEntrada.parse(dataVendaStr);
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data inválida. Use o formato DD/MM/YYYY.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        java.sql.Date dataVendaSql = new java.sql.Date(dataVendaUtil.getTime());
        
        RegistroVenda registro = new RegistroVenda();
        registro.setNomeCliente(nomeCliente);
        registro.setCpfCliente(cpfCliente);
        registro.setTipoSeguro(tipoSeguro);
        registro.setDataVenda(dataVendaSql);
        registro.setValorApolice(valorApolice);
        registro.setFiel(fiel);
        
        if (modoEdicao) {
        registro.setId(registroEdicao.getId());
        registroDAO.atualizar(registro);
        JOptionPane.showMessageDialog(this, "Registro atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } 
        else {
        registroDAO.adicionar(registro);
        JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
        
        txtNomeCliente.setText("");
        txtCPF.setText("");
        txtDataVenda.setText("");
        txtValorApolice.setText("");
        rbtnSim.setSelected(false);
        rbtnNao.setSelected(false);
    }//GEN-LAST:event_btnRegistrarActionPerformed


